package net.tileeditor.general;

public class CountAndDimension
{
  public int xCount;
  public int yCount;
  public int width;
  public int height;

  public CountAndDimension(int xCount, int yCount, int width, int height)
  {
    this.xCount = xCount;
    this.yCount = yCount;
    this.width = width;
    this.height = height;
  }
}
